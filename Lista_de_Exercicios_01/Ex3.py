#Exercício 3 – Antecessor e sucessor
#Faça um programa que receba um número inteiro e mostre:
#o número digitado
#seu antecessor
#seu sucessor

int = int(input("Digite um numero:"))
antecessor = int - 1
sucessor = int + 1
print("O numero digitado foi:", int)
print("O antecessor do numero digitado é:", antecessor)
print("O sucessor do numero digitado é:", sucessor)